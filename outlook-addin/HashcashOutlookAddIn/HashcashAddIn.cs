            if (mailItem != null)
            {
                if (mailItem.EntryID == null)
                {
                    mailItem.Subject = "This text was added by using code";
                    mailItem.Body = "This text was added by using code";
                    mailItem.PropertyAccessor.GetProperty("PR_TRANSPORT_MESSAGE_HEADERS");
                    string[] preparedByArray = mailItem.Headers("From");
                    string preparedBy;
                    if (preparedByArray.Length == 1)
                        preparedBy = preparedByArray[0];
                    else
                        preparedBy = "no one";
                    mailItem.Body += preparedBy;
                }

            }
        }

